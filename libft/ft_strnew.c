#include "libft.h"

char	*ft_strnew(size_t size)
{
	char	*c;
	size_t	i;

	i = 0;
	c = malloc(size + 1);
	if (!c)
		return (NULL);
	while (i <= size)
	{
		c[i] = '\0';
		i++;
	}
	return (c);
}
