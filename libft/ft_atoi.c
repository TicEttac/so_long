#include "libft.h"

int	ft_atoi(const char *s)
{
	long	i[3];

	i[0] = 0;
	i[1] = 1;
	i[2] = 0;
	while ((s[i[0]] <= 13 && s[i[0]] >= 8) || s[i[0]] == ' ')
		i[0]++;
	if (s[i[0]] == '+' || s[i[0]] == '-')
	{
		if (s[i[0]++] == '-')
			i[1] = -1;
		else
			i[1] = 1;
	}
	while (s[i[0]] <= '9' && s[i[0]] >= '0')
	{
		i[2] = (i[2] * 10) + (s[i[0]] - 48);
		i[0]++;
	}
	return (i[2] * i[1]);
}
