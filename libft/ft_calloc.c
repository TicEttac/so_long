#include "libft.h"

void	*ft_calloc(size_t c, size_t s)
{
	void	*r;

	if (c == 0 || s == 0)
	{
		s = 1;
		c = 1;
	}
	r = malloc(c * s);
	if (!r)
		return (NULL);
	ft_bzero(r, c * s);
	return (r);
}
