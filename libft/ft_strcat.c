#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int	i;
	int	dlen;

	i = 0;
	dlen = ft_strlen(s1);
	while (s2[i])
	{
		s1[dlen + i] = s2[i];
		i++;
	}
	s1[dlen + i] = '\0';
	return (s1);
}
